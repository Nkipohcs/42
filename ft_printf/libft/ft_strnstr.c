#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	unsigned long	i;
	size_t			j;

	j = 0;
	i = 0;
	if (!haystack && !len)
		return (0);
	if (!*needle)
		return ((char *)haystack);
	while ((haystack[i]) && (size_t)i < len)
	{
		j = 0;
		while (haystack[i] == needle[j] && haystack[i] && i < len)
		{
			i++;
			j++;
		}
		if (!needle[j])
			return ((char *)&haystack[i - j]);
		i = (i - j) + 1;
	}
	return (NULL);
}
